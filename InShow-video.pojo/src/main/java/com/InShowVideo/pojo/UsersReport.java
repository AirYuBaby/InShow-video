package com.InShowVideo.pojo;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.*;
 
@Table(name = "`users_report`")
public class UsersReport implements Serializable {
    private String id;

    private String dealUserId;

    private String dealVideoId;

    private String type;

    private String content;

    private String userid;

    private Date createDate;

    private static final long serialVersionUID = 1L;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDealUserId() {
        return dealUserId;
    }

    public void setDealUserId(String dealUserId) {
        this.dealUserId = dealUserId;
    }

    public String getDealVideoId() {
        return dealVideoId;
    }

    public void setDealVideoId(String dealVideoId) {
        this.dealVideoId = dealVideoId;
    }


    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public Date getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
}